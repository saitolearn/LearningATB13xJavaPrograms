public class _abc_abc_abc_ {

    public static void main(String[] args) {

        System.out.println("'_abc_abc_abc_' -> is valid name since it follows identifier naming rules");
        System.out.println("\n" +
                "#### **Rules to follow when you define an Identifier:**\n" +
                "1. Identifier can contain Alphabets, Digits, and two special symbol i.e. Dollar ($), Underscore (_).\n" +
                "2. First character of an identifier must be an Alphabet or Dollar ($) or Underscore (_).\n" +
                "3. Keywords or Reserved words **can't be used as Identifiers.**\n");
    }

}
