package ex02_Java_Basics;

public class Lab015_Variables {

    public static void main(String[] args) {

        // data_type variable_name = variable_value
        /*data_type = boolean/byte/char/short/int/long/float/double
        * variable_name = IDENTIFIER
        * variable_value = LITERAL */

        byte b = 10;
        /*byte -> data_type -> Type of container
        stores only integer value but limited to max and min -128 to 127.

        b -> identifier
        = -> assign the value to b
        10 -> literal */

        System.out.println("Value of b is "+ b);

    }
}
