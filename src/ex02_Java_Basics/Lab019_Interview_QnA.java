package ex02_Java_Basics;

public class Lab019_Interview_QnA {

    public static void main(String[] args) {

        int _2 = 120;
        System.out.println(_2);

        int _1 = 110;
        int _0 = 100;

        //These variable names as "_0/_1/_2" are valid
        //Java allows variable names to start with an underscore (_) or a letter, followed by letters, digits, or underscores.

        System.out.println(_1);
        System.out.println(_0);

    }
}
